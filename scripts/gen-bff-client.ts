import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'

import { parse as parseYaml } from 'yaml'

type OpenApiInfo = {
  title: string
  version: string
}

type OpenApiSpec = {
  openapi: string
  info: OpenApiInfo
  paths: Record<string, OpenApiPathItem>
} & Record<string, unknown>

const VERBOSE = process.env.VERBOSE === '1'

function logVerbose(...args: unknown[]) {
  if (VERBOSE) {
    console.log('[gen-bff-client]', ...args)
  }
}

const BFF_OUTPUT_FILE_PATH = path.resolve(
  process.cwd(),
  'app/generated/bff-api-types.ts'
)

type HttpMethod =
  | 'get'
  | 'put'
  | 'post'
  | 'delete'
  | 'options'
  | 'head'
  | 'patch'
  | 'trace'

const HTTP_METHODS: ReadonlyArray<HttpMethod> = [
  'get',
  'put',
  'post',
  'delete',
  'options',
  'head',
  'patch',
  'trace'
]

type TreeOperation = {
  method: HttpMethod
  path: string
}

type TreeNode = {
  children: Record<string, TreeNode>
  operations: Array<TreeOperation>
}

type OpenApiPathItem = Partial<Record<HttpMethod, unknown>> &
  Record<string, unknown>

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === 'object' && value !== null
}

function createNode(): TreeNode {
  return {
    children: {},
    operations: []
  }
}

function escapeStringValue(value: string): string {
  return value
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\n/g, '\\n')
    .replace(/\r/g, '\\r')
    .replace(/\t/g, '\\t')
}

function sanitizePathSegment(segment: string): string {
  const dynamicMatch = segment.match(/^\{(.+)\}$/)
  const normalizedSegment = dynamicMatch ? `$${dynamicMatch[1]}` : segment

  const withoutInvalidChars = normalizedSegment.replace(/[^A-Za-z0-9_$]/g, '_')
  const withValidStart = /^[A-Za-z_$]/.test(withoutInvalidChars)
    ? withoutInvalidChars
    : `_${withoutInvalidChars}`

  return withValidStart
}

function addPathOperation(
  root: TreeNode,
  openApiPath: string,
  method: HttpMethod
): void {
  const segments = openApiPath.split('/').filter(Boolean)
  let currentNode = root

  for (const segment of segments) {
    const childKey = sanitizePathSegment(segment)

    if (!currentNode.children[childKey]) {
      currentNode.children[childKey] = createNode()
    }

    currentNode = currentNode.children[childKey]
  }

  currentNode.operations.push({
    method,
    path: openApiPath
  })
}

function buildPathTree(paths: Record<string, OpenApiPathItem>): TreeNode {
  const root = createNode()

  for (const [openApiPath, pathItem] of Object.entries(paths)) {
    if (!isRecord(pathItem)) {
      continue
    }

    for (const method of HTTP_METHODS) {
      if (pathItem[method]) {
        addPathOperation(root, openApiPath, method)
      }
    }
  }

  return root
}

function sortNode(node: TreeNode): TreeNode {
  for (const child of Object.values(node.children)) {
    sortNode(child)
  }

  node.children = Object.fromEntries(
    Object.entries(node.children).sort(([keyA], [keyB]) =>
      keyA.localeCompare(keyB)
    )
  )

  node.operations.sort((operationA, operationB) => {
    const methodIndexA = HTTP_METHODS.indexOf(operationA.method)
    const methodIndexB = HTTP_METHODS.indexOf(operationB.method)

    return methodIndexA - methodIndexB
  })

  return node
}

function renderNode(node: TreeNode, depth = 1): Array<string> {
  const indentation = '  '.repeat(depth)
  const lines: Array<string> = []

  for (const [childKey, childNode] of Object.entries(node.children)) {
    lines.push(`${indentation}${childKey}: {`)
    lines.push(...renderNode(childNode, depth + 1))
    lines.push(`${indentation}},`)
  }

  for (const operation of node.operations) {
    const clientMethod = operation.method.toUpperCase() as Uppercase<HttpMethod>
    const escapedPath = escapeStringValue(operation.path)

    lines.push(
      `${indentation}${operation.method}: ((...init: MethodArgs<'${operation.method}', '${escapedPath}'>) =>`
    )
    lines.push(
      `${indentation}  client.${clientMethod}('${escapedPath}', ...init)) as LeafMethod<'${operation.method}', '${escapedPath}'>,`
    )
  }

  return lines
}

