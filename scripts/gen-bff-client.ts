function validateStringField(
  value: unknown,
  fieldName: string,
  source: string
): string {
  if (!value || typeof value !== 'string') {
    throw new Error(
      `Invalid OpenAPI spec from ${source}: missing or invalid '${fieldName}' field`
    )
  }
  return value
}

function validateOpenApiSpec(spec: unknown, source: string): OpenApiSpec {
  if (!isRecord(spec)) {
    throw new Error(`Invalid OpenAPI spec from ${source}: not an object`)
  }

  validateStringField(spec.openapi, 'openapi', source)

  if (!isRecord(spec.info)) {
    throw new Error(
      `Invalid OpenAPI spec from ${source}: 'info' must be an object`
    )
  }

  const title = validateStringField(spec.info.title, 'info.title', source)
  const version = validateStringField(spec.info.version, 'info.version', source)

  if (!isRecord(spec.paths)) {
    throw new Error(
      `Invalid OpenAPI spec from ${source}: 'paths' must be an object`
    )
  }

  const pathCount = Object.keys(spec.paths).length
  logVerbose(`Validated OpenAPI spec: ${title} v${version}, ${pathCount} paths`)

  return spec as OpenApiSpec
}

async function loadSpec(specPath: string): Promise<OpenApiSpec> {
  const absoluteSpecPath = path.resolve(process.cwd(), specPath)
  const specContents = await readFile(absoluteSpecPath, 'utf8')
  logVerbose(`Read OpenAPI spec from ${absoluteSpecPath}`)

  const extension = path.extname(specPath).toLowerCase()

  let spec: unknown
  if (extension === '.yaml' || extension === '.yml') {
    spec = parseYaml(specContents)
  } else {
    try {
      spec = JSON.parse(specContents)
    } catch {
      spec = parseYaml(specContents)
    }
  }

  return validateOpenApiSpec(spec, absoluteSpecPath)
}

async function main() {
  const specPath = process.env.SPEC_PATH

  if (!specPath) {
    console.error('Error: SPEC_PATH environment variable is required')
    console.error(
      'Usage: SPEC_PATH=path/to/spec.json node scripts/gen-bff-client.ts'
    )
    process.exit(1)
  }

  const spec = await loadSpec(specPath)

  const pathTree = sortNode(buildPathTree(spec.paths))

  logVerbose('Building BFF client type definitions...')
  const bffClientSource = renderBffClient(pathTree)

  await mkdir(path.dirname(BFF_OUTPUT_FILE_PATH), { recursive: true })
  await writeFile(BFF_OUTPUT_FILE_PATH, bffClientSource)

  console.log(`Generated ${BFF_OUTPUT_FILE_PATH}`)
}

main().catch((error: unknown) => {
  console.error(error)
  process.exit(1)
})
